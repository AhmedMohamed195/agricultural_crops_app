/// A class that holds static string paths to all the app images.
/// This class is abstract and final to prevent instantiation and extension.
abstract final class AppImages {
  /// The base path for all image assets.
  static const String basePath = 'assets/images';

  static const String appLogo = '$basePath/app-logo.svg';
  static const String carrot = '$basePath/carrot.png';
  static const String farmer = '$basePath/farmer.svg';
  static const String garlic = '$basePath/garlic.png';
  static const String tomatoImage = '$basePath/tomato-image.png';
  static const String garlic2 = '$basePath/garlic.svg';
  static const String onboardingimage_1 = '$basePath/onboarding_image_1.svg';
  static const String onboardingimage_2 = '$basePath/onboarding_image_2.svg';
  static const String onboardingimage_3 = '$basePath/onboarding_image_3.svg';
  static const String onboardingbackgroundimage_1 =
      '$basePath/onboarding_background_image_1.svg';
  static const String onboardingbackgroundimage_2 =
      '$basePath/onboarding_background_image_2.svg';
  static const String onboardingbackgroundimage_3 =
      '$basePath/onboarding_background_image_3.svg';

}
